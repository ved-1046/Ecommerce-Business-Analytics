#monthly revenue trend
query = """
select date_format(order_purchase_timestamp , '%%Y-%%m') as month,
sum(payment_value) as total_revenue
from orders
inner join payments
on orders.order_id = payments.order_id
group by month
order by month;
"""

df = pd.read_sql(query , engine)
print(df.head())

plt.figure(figsize = (12,6))
plt.plot(df['month'], df['total_revenue'], marker = 'o', color = 'royalblue')
plt.title('Monthly revenue trend')
plt.xlabel('Monthly')
plt.ylabel('Total revenue')
plt.xticks(rotation = 45)
plt.tight_layout()
plt.show()

# =====================================
# Business Insights
# =====================================
# 1. Revenue trends help identify periods of business growth and decline.
# 2. Peaks in revenue may indicate seasonal demand or successful promotional campaigns.
# 3. Understanding monthly trends helps the business plan inventory, staffing, and marketing activities.

#revenue by product category
query = """
select product_category_name , sum(price) as total_revenue
from order_items
inner join products on
order_items.product_id = products.product_id
group by product_category_name
order by total_revenue desc
limit 10;
"""
df = pd.read_sql(query , engine)
print(df.head())

plt.figure(figsize=(12,7))

plt.barh(df['product_category_name'] , df['total_revenue']
         ,color = 'orange')

plt.title('Top 10 categories by revenue')
plt.xlabel('Revenue')
plt.ylabel('Product category')

plt.gca().invert_yaxis() #get current axis and flip the vertical axis
plt.tight_layout()
plt.show()

# =====================================
# Business Insights
# =====================================
# 1. A few product categories contribute a significant portion of total revenue.
# 2. High-performing categories should remain a priority for inventory and promotions.
# 3. Low-performing categories can be reviewed for pricing, marketing, or product improvements.

#Top 10 citites by revenue
