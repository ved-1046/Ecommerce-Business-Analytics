#sqlalchemy acts a bridge between python and mysql
#pymysql - python communicates with mysql server here
import pandas as pd
import matplotlib.pyplot as plt
from sqlalchemy import create_engine #creates the connection

engine = create_engine("mysql+pymysql://root:password@localhost:3306/ecommerce_analysis")

tables = {
     "customers": "olist_customers_dataset.csv",
    "orders": "olist_orders_dataset.csv",
    "order_items": "olist_order_items_dataset.csv",
    "payments": "olist_order_payments_dataset.csv",
    "products": "olist_products_dataset.csv",
    "reviews": "olist_order_reviews_dataset.csv",
    "sellers": "olist_sellers_dataset.csv",
    "geolocation": "olist_geolocation_dataset.csv",
    "category_translation": "product_category_name_translation.csv"
}
for table_name , file_name in tables.items():
    df = pd.read_csv(file_name)

    df.to_sql(
        name = table_name,
        con= engine,
        if_exists='replace',
        index=False
    )

print(f"{table_name} imported successfully")
print("\n all tables imported successfully")


#customers 
query = """
select count(*) from customers
"""
df = pd.read_sql(query,engine)
print(df)

#which state has highest number of customers
query = """
select customer_state, count(*) as total_customers
from customers
group by customer_state
order by total_customers desc;
"""

df = pd.read_sql(query,engine)
print(df)


plt.figure(figsize = (10,6))
plt.bar(df['customer_state'], df['total_customers'])
plt.title('Customers by state')
plt.xlabel('State')
plt.ylabel('Number of customers')

plt.show()

# Business Insight:
# SP has the highest number of customers,
# indicating the company's strongest customer base is in this state.

#revenue analysis
query = """
select customer_state , sum(payment_value) as total_revenue
from customers
inner join orders
on customers.customer_id = orders.customer_id 
inner join payments
on orders.order_id = payments.order_id
group by customer_state
order by total_revenue desc;
"""

df = pd.read_sql(query,engine)
print(df)

plt.figure(figsize= (10,6))
plt.bar(df['customer_state'] , df['total_revenue'])

plt.title("Revenue generated by state")
plt.xlabel("Customer state")
plt.ylabel('Total Revenue')
plt.xticks(rotation = 45)
plt.tight_layout()

plt.show()
# =====================================
# Business Insight
# =====================================
# 1. SP generates the highest revenue among all states.
# 2. Revenue is unevenly distributed across states.
# 3. The company may focus on growing lower-performing states.


